def export_results_json(results: list[DebateResult], filepath: str = "results.json") -> None:
    """Export results to JSON for further analysis."""
    export_data = []
    for idx, r in enumerate(results):
        export_data.append({
            "case_id": idx,
            "claim": r.claim,
            "truth": r.truth,
            "verdict": r.final_verdict.value,
            "confidence": r.confidence,
            "reasoning": r.verdict_reasoning,
            "prosecutor": {
                "agent_name": r.prosecutor_response.agent_name,
                "arguments": r.prosecutor_response.arguments,
                "evidence": r.prosecutor_response.evidence,
                "confidence": r.prosecutor_response.confidence,
                "mutation_types": r.prosecutor_response.mutation_types or []
            },
            "defense": {
                "agent_name": r.defense_response.agent_name,
                "arguments": r.defense_response.arguments,
                "evidence": r.defense_response.evidence,
                "confidence": r.defense_response.confidence
            },
            "epistemologist": {
                "agent_name": r.epistemologist_response.agent_name,
                "arguments": r.epistemologist_response.arguments,
                "evidence": r.epistemologist_response.evidence,
                "confidence": r.epistemologist_response.confidence
            }
            # "debate_transcript": r.debate_transcript
        })

    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(export_data, f, indent=2, ensure_ascii=False)

    print(f"âœ… Results exported to {filepath}")


